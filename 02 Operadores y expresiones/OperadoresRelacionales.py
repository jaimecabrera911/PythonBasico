print("Operadores Logicos numeros")
a = 5
b = 6

print("Igualdad")
print(a == b)

print("Desigualdad")
print(a != b)

print("Mayor que")
print(a > b)

print("Menor que")
print(a < b)

print("Mayor o igual que")
print(a + 1 >= b)

print("Menor o igual que")
print(a <= b)

print("\nOperadores Logicos strings")

print("Hola" == "Hola")
print("Hola" == "HOla")

c = "Hola"
print(c[0] == "H")

l1 = [0, 1, 2]
l2 = [2, 3, 4]

print(l1 == l2)
print(len(l1) == len(l2))
