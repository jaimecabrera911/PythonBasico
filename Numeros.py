a = 10
b = 4
suma = a + b
print("Suma=", suma)  # El resultado es un int o entero

resta = a - b
print("Resta=", resta)

multiplicacion = a * b
print("Multiplicacion=", multiplicacion)

division = a / b
print("Division=", division)  # El resultado es un float

modulo = a % b  # el modulo es el residuo de la division
print("Modulo=", modulo)

potencia = 4 ** 4
print("Potencia=", potencia)

raiz = 16 // 4
print("Raiz=", raiz)

